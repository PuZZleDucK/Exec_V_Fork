package com.puzzleduck.acrawl;


public class AppData
{

  private String appName;
  private String appUrl;
  private String appDescription;


  public AppData()
  {
    appName = "Null";
  }

  public AppData(String newName)
  {
    appName = newName;
  }

  public AppData(String newName, String newUrl)
  {
    appName = newName;
    appUrl = newUrl;
  }




}//class
